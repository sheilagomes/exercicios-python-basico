#separar strings com for
for letra in 'aeiou': print (letra)

#separar strings com while
texto = 'aeiou'
k = 0
while k < len(texto):
    letra = texto[k]
    print (letra)
    k = k + 1

#separar listas com for
for x in ['aeiou', 34, 3.14]: print (x)

#separar strings com while
lista = ['aeiou', 34, 3.14]
k = 0
while k < len(lista):
    x = lista[k]
    print (x)
    k = k + 1

